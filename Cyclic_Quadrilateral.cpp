#include <bits/stdc++.h>
int main()
{
    int t; std::cin >> t;
    while(t--)
    {
        long int a,b,c,d; std::cin >> a >> b >> c >> d;
        if(a+c==180 && b+d==180)
        {
            std::cout << "YES" << std::endl;
        }else
        {
            std::cout << "NO" << std::endl;
        }
    }
    return 0;
}